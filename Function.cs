class Calculation{
    public int Add(int A, int B)  
    {  
        return A + B;
    }  
    public int Multiply(int A, int B)  
    {  
        return A * B;  
    }  
    public int Subtract(int A, int B)  
    {  
            if (A > B)  
            {  
                return A - B;  
            }  
  
            return A - B;  

        }  
    public float Division(float A, float B)   
    {  
        return A / B;  
    }  
  
}  
